    public static void paintMap(Graphics g) {
        int y = GameUtil.FRAME_BEGIN_Y;
        int space = GameUtil.MOVING_PIXELS;
        Image image;
        for (int[] rows : map1) {
            int x = GameUtil.FRAME_BEGIN_X;
            for (int row : rows) {
                switch (row) {
                    case 0:
                        image = null;
                        break;
                    case 1:
                        image = GameUtil.getImage("img/wall/water.gif");
                        break;
                    case 2:
                        image = GameUtil.getImage("img/wall/grass.gif");
                        break;
                    case 3:
                        image = GameUtil.getImage("img/wall/steel.gif");
                        break;
                    case 4:
                        image = GameUtil.getImage("img/wall/wall.gif");
                        break;
                    case 5:
                        image = null;
                        break;
                    default:
                        image = null;
                }
                if (image != null) {
                    g.drawImage(image, x, y, null);
                }
                x += space;
            }
            y += space;
        }
    }
