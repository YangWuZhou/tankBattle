    public static void paintMap(Graphics g) {
        int y = Constant.FRAME_BEGIN_Y;
        int space = Constant.SPACING;
        Image image = null;
        for (int[] rows : map) {
            int x = Constant.FRAME_BEGIN_X;
            for (int row : rows) {
                switch (row) {
                    case 1:
                        image = GameUtil.getImage(FilePrefix.OBSTACLE_IMG + "redBrick.png");
                        break;
                    case 2:
                        image = GameUtil.getImage(FilePrefix.OBSTACLE_IMG + "steelBrick.png");
                        break;
                    case 3:
                        image = GameUtil.getImage(FilePrefix.OBSTACLE_IMG + "river.png");
                        break;
                    case 4:
                        image = GameUtil.getImage(FilePrefix.OBSTACLE_IMG + "grassland.png");
                        break;
                    case 5:
                        image = GameUtil.getImage(FilePrefix.OBSTACLE_IMG + "snowfield.png");
                        break;
                    case 6:
                        image = GameUtil.getImage(FilePrefix.OBSTACLE_IMG + "eagle.png");
                        break;
                    default:
                        image = null;
                }
                if (image != null) {
                    g.drawImage(image, x, y, null);
                }
                x += space;
            }
            y += space;
        }
    }
